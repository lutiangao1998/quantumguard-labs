"""
QuantumGuard Labs - Post-Quantum Cryptography Hybrid Signatures
================================================================
Implements real PQC signing and verification using the Open Quantum Safe (OQS)
liboqs library, plus hybrid (classical + PQC) signature schemes for a smooth
transitional security model.

Hybrid signature rationale:
  A hybrid signature combines a classical signature (ECDSA/Schnorr) with a
  post-quantum signature (ML-DSA / Falcon) in a single envelope. This ensures:
    1. Security against classical adversaries (via ECDSA/Schnorr).
    2. Security against quantum adversaries (via ML-DSA / Falcon).
    3. Backward compatibility during the transition period.

Supported schemes:
  - ML-DSA-44  (NIST FIPS 204, security level 2, ~128-bit quantum security)
  - ML-DSA-65  (NIST FIPS 204, security level 3, ~192-bit quantum security)
  - ML-DSA-87  (NIST FIPS 204, security level 5, ~256-bit quantum security)
  - Falcon-512 (NIST FIPS 206, security level 1, compact signatures)
  - Falcon-1024(NIST FIPS 206, security level 5, compact signatures)
  - Hybrid ECDSA-secp256k1 + ML-DSA-44
  - Hybrid Schnorr-BIP340  + ML-DSA-65

Reference: https://openquantumsafe.org/
"""

